export function isNightHours(): boolean {
  const hour = new Date().toLocaleString("en-US", {
    timeZone: "America/Toronto",
    hour: "2-digit",
    hour12: false,
  });
  const h = parseInt(hour, 10);
  return h >= 1 && h < 6;
}
